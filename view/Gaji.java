    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        txId_Karyawan.getText();
        
        try{
            Connection c = koneksi.getKoneksi();
                String query = "SELECT*FROM data_karyawan where id_karyawan='"+txId_Karyawan.getText()+"'";
                Statement st = c.createStatement();
                ResultSet rs = st.executeQuery(query);

                while(rs.next()){
                    txNama.setText(rs.getString("Nama"));
                    txPosisi.setText(rs.getString("Jabatan"));
                    txGaji_pokok.setText(rs.getString("Gaji_pokok"));
        }
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        int Gaji_pokok = Integer.valueOf(txGaji_pokok.getText());
        int Lembur = Integer.valueOf(txLembur.getText());
        int Bonus = Integer.valueOf(txBonus.getText());
        int Asuransi = Integer.valueOf(txAsuransi.getText());
        Lembur = Lembur*10000;
        
        int Gaji_bersih;
        Gaji_bersih = (Gaji_pokok+Lembur+Bonus)-Asuransi;
        txGaji_bersih.setText(String.valueOf(Gaji_bersih));
    }//GEN-LAST:event_btnHitungActionPerformed

