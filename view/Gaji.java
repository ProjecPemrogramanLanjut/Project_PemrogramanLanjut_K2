    private void simpan(){
        try {
            java.sql.Connection c = koneksi.getKoneksi();
            String sql = "INSERT INTO gaji_karyawan VALUES ('"+txId_Karyawan.getText()+"','"+txNama.getText()+"',"
                    + "'"+txPosisi.getText()+"','"+txMasa.getText()+"','"+txGaji_pokok.getText()+"','"+txLembur.getText()+"',"
                    + "'"+txAsuransi.getText()+"','"+txBonus.getText()+"','"+txGaji_bersih.getText()+"','"+txTanggal.getText()+"')";
            java.sql.PreparedStatement pst=c.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
