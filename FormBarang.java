    private void btnhpsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhpsActionPerformed
        // TODO add your handling code here:
        // Menghapus data
        try{
            if((txtKode_buku.getText().equals(""))
                || (txtJudul.getText().equals(""))
                || (txtGenre.getSelectedItem().equals(""))
                || (txtPengarang.getText().equals(""))
                || (txtPenerbit.getText().equals(""))
                || (txtTahun_terbit.getText().equals(""))
                || (txtStok.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di hapus!");
            }else{
                String sql="DELETE FROM tb_buku WHERE Kode_buku='"+txtKode_buku.getText()+"'";
                PreparedStatement stat = conn.prepareStatement(sql);

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                bersih();
                datatable();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus"+e);
        }
    }//GEN-LAST:event_btnhpsActionPerformed

