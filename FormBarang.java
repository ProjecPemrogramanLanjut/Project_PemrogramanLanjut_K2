    private void datatable(){
        //Menampilkan data dari database
        Object[] Baris = {"Kode Buku", "Judul", "Genre", "Pengarang", "Penerbit", "Tahun_terbit", "Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtCari.getText();
        try {
            String sql = "SELECT Kode_buku,Judul,Genre,Pengarang,Penerbit,Tahun_terbit, Stok,Tanggal_masuk "
                    + "FROM tb_buku WHERE Kode_buku like '%"+cariitem+"%' or Judul like "
                    + "'%"+cariitem+"%' or Genre like '%"+cariitem+"%' or Pengarang like '%"+cariitem+"%' "
                    + "or Penerbit like '%"+cariitem+"%' or Tahun_terbit like '%"+cariitem+"%' "
                    + "or Stok like '%"+cariitem+"%' order by Kode_buku asc";
            java.sql.Statement stamt = conn.createStatement();
            ResultSet hasil = stamt.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
                hasil.getString(6),
                hasil.getString(7)
            });
            }
            tbl.setModel(tabmode);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dipanggil" + e);
        }
    }

